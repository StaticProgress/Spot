void kprint(UINT8 *frame_base, UINTN x, UINTN y, UINT32 color, char *str) {
	UINTN offset = y * 1024 + x;
	for (char *char_ptr = str; *char_ptr != '\0'; char_ptr++) {
		if (31 < (*char_ptr) && (*char_ptr) < 127) {
			kprint_char(frame_base, offset, color, *char_ptr);
			offset += 9;
		} else if (*char_ptr == '\n') {
			y += 14;
			offset = y * 1024 + x;
		}
	}
}
